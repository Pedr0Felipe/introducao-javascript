
let arr03 = [1, 2, 3, 3, 4, 3];     // ARRAY criado com vários elementos iguais alocados em índices diferentes.

console.log(arr03.indexOf(3));      // A busca utilizando o método indexOf terá como resultado a localização do elemento pela ordem inicial do índice.

console.log(arr03.lastIndexOf(3));  // A busca utilizando o método lastIndexOf terá como resultado a localização do elemento pela ordem final do índice.